def sql_quote(s: str) -> str:
    # safe enough for our controlled strings: single quotes doubled
    return "'" + s.replace("'", "''") + "'"


def main() -> None:
    ap = argparse.ArgumentParser(
        description="Seed Lotus-Warden Postgres with Semgrep-like findings (evidence+raw_data)."
    )
    ap.add_argument(
        "--count",
        type=int,
        default=100_000,
        help="Сколько findings добавить (default: 100000)",
    )
    ap.add_argument(
        "--chunk", type=int, default=20_000, help="Размер чанка (default: 20000)"
    )
    ap.add_argument(
        "--tenant-id",
        default=os.getenv("TENANT_ID"),
        help="Tenant UUID (если не задан — root@localhost или default)",
    )
    ap.add_argument(
        "--product-id",
        default=os.getenv("PRODUCT_ID"),
        help="Product UUID (если не задан — создадим/найдем stress-seed)",
    )
    ap.add_argument(
        "--scanner",
        default=DEFAULT_SCANNER,
        help="scanner для scan_results (default: semgrep)",
    )
    ap.add_argument(
        "--cleanup",
        action="store_true",
        help="Удалить ранее созданные stress-seed scan_results (+ findings каскадом) и выйти",
    )
    args = ap.parse_args()

    if args.count <= 0 and not args.cleanup:
        raise RuntimeError("--count должен быть > 0")

    compose_cmd = detect_compose_cmd()
    ensure_postgres_running(compose_cmd)
    ensure_extensions(compose_cmd)

    tenant_id = resolve_tenant_id(compose_cmd, args.tenant_id)
    product_id = resolve_or_create_product(compose_cmd, tenant_id, args.product_id)

    print(f"[i] tenant_id={tenant_id}")
    print(f"[i] product_id={product_id}")
    print(f"[i] scanner={args.scanner}")

    if args.cleanup:
        cleanup_seed(compose_cmd, tenant_id, product_id, args.scanner)
        return

    scan_result_id = create_scan_result(
        compose_cmd, tenant_id, product_id, args.scanner
    )
    print(f"[i] scan_result_id={scan_result_id}")

    seed = uuid.uuid4().hex
    print(f"[i] seeding {args.count} findings (seed={seed}) ...")

    seed_semgrep_findings(
        compose_cmd=compose_cmd,
        tenant_id=tenant_id,
        product_id=product_id,
        scan_result_id=scan_result_id,
        count=args.count,
        chunk=args.chunk,
        seed=seed,
    )

    psql(compose_cmd, "ANALYZE findings;")
    psql(compose_cmd, "ANALYZE scan_results;")

    total = psql(
        compose_cmd,
        """
        SELECT COUNT(*)::text
        FROM findings
        WHERE tenant_id = :'tenant'::uuid
          AND product_id = :'product'::uuid
          AND (duplicate_id IS NULL OR duplicate_id IS NULL) -- tolerate schema variance
          AND (deleted_at IS NULL OR deleted_at IS NULL);
        """,
        vars={"tenant": tenant_id, "product": product_id},
    ).strip()

    print(f"[ok] total findings in product now (rough): {total}")
    print(
        f"[i] cleanup: python stress_seed_findings.py --cleanup --scanner {args.scanner}"
    )


if __name__ == "__main__":
    try:
        main()
    except Exception as e:
        print(f"[ERR] {e}", file=sys.stderr)
        sys.exit(1)
