    gh_expr = "${{{{ inputs.box_ticked }}}}"  # -> "${{ inputs.box_ticked }}"
    shell_var = "${{BOX_TICKED}}"  # -> "${BOX_TICKED}"
    code_line = f'      - run: echo "was the box ticked? {shell_var}! ({gh_expr})"'

